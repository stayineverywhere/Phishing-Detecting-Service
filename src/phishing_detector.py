def keyword_score(text):
    score = 0

    # 리스트 점수 (+1)
    for word in keywords_list:
        if word in text:
            score += 1

    # 딕셔너리 추가 점수 (+가중치)
    for word, weight in keywords_dict.items():
        if word in text:
            score += weight

    # ✔ 최대 점수로 나눠서 0~1 정규화
    max_score = len(keywords_list) + sum(keywords_dict.values())
    return score / max_score

