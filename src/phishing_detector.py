def context_score(text):
    score = 0

    # 기관 + 송금
    if ("검찰" in text or "경찰" in text) and ("송금" in text or "돈 보내" in text):
        score += 40

    # 인증번호 요구
    if "인증번호" in text:
        score += 30

    # 설치 요구
    if "설치" in text:
        score += 30

    return min(score, 100) / 100  # 0~1

"""#6. 최종 결합"""

import math

def sigmoid(x):
    return 1 / (1 + math.exp(-x))


def final_probability(text):
    K = keyword_score(text)
    P = pattern_score(text)
    AI = ai_score(text)
    U = urgency_score(text)
    C = context_score(text)

    z = -3 + 5*K + 5*P + 10*AI + 4*U + 5*C
    prob = sigmoid(z)

    return prob, {
        "K": K,
        "P": P,
        "AI": AI,
        "U": U,
        "C": C,
        "z": z
    }


def is_phishing_final(prob, K, P, AI, U, C):

    if prob < 0.75:
        return False, prob

    if AI < 0.4:
        return False, prob

    risk_signal = (P > 0.4) or (U > 0.5) or (K > 0.3)

    if not risk_signal:
        return False, prob

    return True, prob

